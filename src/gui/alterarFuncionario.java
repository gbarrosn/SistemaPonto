    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:

        String nome = jTextFieldNome.getText().trim();

        int matricula = 0;
        String matriculaStr = jTextFieldMatricula.getText().trim();
        if (matriculaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "O campo matrícula é obrigatório", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            try {
                matricula = Integer.parseInt(matriculaStr);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "O campo matrícula deve ser um número", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        //int id = funcionarioAlterar.getIdFuncionario();
        String setor = jComboBoxSetor.getSelectedItem().toString();
        String turno = jComboBoxTurno.getSelectedItem().toString();
        String funcao = jComboBoxFuncao.getSelectedItem().toString();
        String dataAdmissao = jTextFieldDataAdmissao.getText().trim();
        String escala = jComboBoxEscala.getSelectedItem().toString();
        String horario = jComboBoxEntrada.getSelectedItem().toString() + " - " + jComboBoxSaida.getSelectedItem().toString();
        String horasSemanais = jComboBoxSemanal.getSelectedItem().toString();
        String codigoDeBarras = jTextFieldCodigoBarras.getText().trim();
        String senha = jPasswordField1.getText().trim();
        String vinculo = jComboBoxVinculo.getSelectedItem().toString();
        String contrato = jComboBoxContrato.getSelectedItem().toString();
        String empresa = jComboBoxEmpresa.getSelectedItem().toString();


        funcionario novoFuncionario = new funcionario();
        novoFuncionario.setIdFuncionario(idFuncionario); 
        novoFuncionario.setNome(nome);
        novoFuncionario.setMatricula(matricula);
        novoFuncionario.setSetor(setor);
        novoFuncionario.setTurno(turno);
        novoFuncionario.setFuncao(funcao);
        novoFuncionario.setDataAdmissao(dataAdmissao);
        novoFuncionario.setEscala(escala);
        novoFuncionario.setHorario(horario);
        novoFuncionario.setHorasSemanais(horasSemanais);
        novoFuncionario.setCodigoDeBarras(codigoDeBarras);
        novoFuncionario.setSenha(senha);
        novoFuncionario.setVinculo(vinculo);
        novoFuncionario.setContrato(contrato);
        novoFuncionario.setEmpresa(empresa);


        if (novoFuncionario.getVinculo().equals("Servidor")) {
            novoFuncionario.setContrato(" - ");
            novoFuncionario.setServidor(true);
        } else if (novoFuncionario.getVinculo().equals("CTD")) {
            novoFuncionario.setContrato("CTD");
        } 

        if (novoFuncionario.getFuncao().equals("Coordenação") || novoFuncionario.getFuncao().equals("Assistente Coordenação")) {
            novoFuncionario.setAdm(true);
        } else {
            novoFuncionario.setAdm(false);
        }

        // Check if any field in novoFuncionario is empty TODO: REPLICAR A OTIMIZAÇÃO NAS OUTRAS CLASSES
        for (java.lang.reflect.Field field : novoFuncionario.getClass().getDeclaredFields()) {
            field.setAccessible(true);
            try {
                Object value = field.get(novoFuncionario);
                if (value == null || value.toString().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "O campo " + field.getName() + " é obrigatório", "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } catch (IllegalAccessException ex) {
                // Handle exception
            }
        }

        try {
            dadosFuncionario.alterarFuncionario(novoFuncionario);
            JOptionPane.showMessageDialog(this, "Funcionário alterado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            listarFuncionarios tela = new listarFuncionarios();
            tela.setVisible(true);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar o funcionário: " + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

