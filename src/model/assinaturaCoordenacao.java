public class assinaturaCoordenacao {
    private int idAssinatura;
    private int idFuncionario;
    private int idCoordenacao;
    private int mes;
    private String dataAssinatura;
    private String horaAssinatura;

    public int getIdAssinatura() {
        return idAssinatura;
    }

    public void setIdAssinatura(int idAssinatura) {
        this.idAssinatura = idAssinatura;
    }

    public int getIdFuncionario() {
        return idFuncionario;
    }

    public void setIdFuncionario(int idFuncionario) {
        this.idFuncionario = idFuncionario;
    }

    public int getIdCoordenacao() {
        return idCoordenacao;
    }

    public void setIdCoordenacao(int idCoordenacao) {
        this.idCoordenacao = idCoordenacao;
    }

    public int getMes() {
        return mes;
    }

    public void setMes(int mes) {
        this.mes = mes;
    }

    public String getDataAssinatura() {
        return dataAssinatura;
    }

    public void setDataAssinatura(String dataAssinatura) {
        this.dataAssinatura = dataAssinatura;
    }

    public String getHoraAssinatura() {
        return horaAssinatura;
    }

    public void setHoraAssinatura(String horaAssinatura) {
        this.horaAssinatura = horaAssinatura;
    }
}
