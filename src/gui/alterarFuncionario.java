    public alterarFuncionario(funcionario funcionarioAlterar) {
        initComponents();

        idFuncionario = funcionarioAlterar.getIdFuncionario();
        jTextFieldNome.setText(funcionarioAlterar.getNome());
        jTextFieldMatricula.setText(String.valueOf(funcionarioAlterar.getMatricula()));
        jComboBoxFuncao.setSelectedItem(funcionarioAlterar.getFuncao());
        jTextFieldDataAdmissao.setText(funcionarioAlterar.getDataAdmissao());
        jComboBoxEscala.setSelectedItem(funcionarioAlterar.getEscala());
        jComboBoxEntrada.setSelectedItem(funcionarioAlterar.getHorario().split(" - ")[0]);
        jComboBoxSaida.setSelectedItem(funcionarioAlterar.getHorario().split(" - ")[1]);
        jComboBoxTurno.setSelectedItem(funcionarioAlterar.getTurno());
        jComboBoxSemanal.setSelectedItem(funcionarioAlterar.getHorasSemanais());
        jTextFieldCodigoBarras.setText(funcionarioAlterar.getCodigoDeBarras());
        jPasswordField1.setText(funcionarioAlterar.getSenha());
        jComboBoxEmpresa.setSelectedItem(funcionarioAlterar.getEmpresa());

        List<String> contratos = null;
        try {
            contratos = dadosContrato.buscarContratos();
        } catch (SQLException ex) {
            System.getLogger(alterarFuncionario.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
        
        int index = 0;
        
        while (index < contratos.size()) {
            jComboBoxContrato.addItem(contratos.get(index));
            index ++;
        }
        
        jComboBoxContrato.addItem(funcionarioAlterar.getContrato());
        jComboBoxContrato.setSelectedItem(funcionarioAlterar.getContrato());

        if (!funcionarioAlterar.isServidor()) {
            jComboBoxVinculo.setSelectedItem("Terceirizado");
        }

        if (funcionarioAlterar.getVinculo() != null) {
            if (funcionarioAlterar.getVinculo().equals("Servidor")) {
            jComboBoxVinculo.setSelectedItem("Servidor");
            } else if (funcionarioAlterar.getVinculo().equals("CTD")) {
            jComboBoxVinculo.setSelectedItem("CTD");
            } else if (funcionarioAlterar.getVinculo().equals("Terceirizado")) {
            jComboBoxVinculo.setSelectedItem("Terceirizado");
            } else if (funcionarioAlterar.getVinculo().equals("Terceirizado Órgão")) {
            jComboBoxVinculo.setSelectedItem("Terceirizado Órgão");
            }
        }

        if (funcionarioAlterar.getFuncao() != null) {
            jComboBoxFuncao.setSelectedItem(funcionarioAlterar.getFuncao());
        }

        
        

        jComboBoxSetor.setSelectedItem(funcionarioAlterar.getSetor());

    }

