        // TODO add your handling code here:
        String senhaAntiga = new String(jPasswordFieldAntiga.getPassword());
        String senhaNova = new String(jPasswordFieldNova.getPassword());
        String senhaNova2 = new String(jPasswordFieldNova2.getPassword());

        if (senhaNova.equals(senhaNova2)) {

            if (senhaFuncionario.equals(senhaAntiga)) {
                try {
                    dadosFuncionario.alterarSenha(idFuncionario, senhaNova);
                    JOptionPane.showMessageDialog(null, "Senha alterada com sucesso");
                    listarFuncionarios listarFuncionarios = new listarFuncionarios();
                    listarFuncionarios.setVisible(true);
                    this.dispose();

                } catch (SQLException e) {
                    
                    e.printStackTrace();
                }
                
            } else {
                JOptionPane.showMessageDialog(null, "Senha antiga incorreta");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Senhas n√£o conferem");
        }
